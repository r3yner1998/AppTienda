    private boolean validarVacios(String[] textos) {
        for (String texto : textos) {
            if (texto.isBlank()) {
                return false;
            }

        }
        return true;
    }
    public void limpiar(){
        this.txtCorreo.setText("");
        this.txtDoc.setText("");
        this.txtNombre.setText("");
        this.txtTelefono.setText("");
    }

